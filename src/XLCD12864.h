void lcd_pos()
{               
	pos++;
	imy = pos / 128;	
	imx = pos % 128;
	if(old_imy != imy) {
		old_imy = imy;
		pos += 8;			//skip cusor position
		imy = pos/128;	
		imx = pos%128;
		
		LCD_CS1_ON;    //LCD_CS1 = ON;
		LCD_CS2_ON;    //LCD_CS2 = ON;
		put_lcd_cmd(YADDR + imy);
	} 
	if(imx == 64) {								
		LCD_CS1_OFF;
		LCD_CS2_ON;
		put_lcd_cmd(XADDR + (imx - 64));
	} else if(imx == 8) {								
		LCD_CS2_OFF;
		LCD_CS1_ON;
		put_lcd_cmd(XADDR + imx);
	}
}
